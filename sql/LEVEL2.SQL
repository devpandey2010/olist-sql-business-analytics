--Q2.Repeat Customer Revenue Contribution

/* repeat Purchase rate means total amount purchased by repat customers/
total revenue generated by this we can find repeat purchase rate of customers
 */
--**Insights**

/* If Repeat Purchase rate is High then the business is stable making good revenue 
If Repeat Purchase Rate is low then business is mostly dependent on new customers and having low AOV of repeat customers which is costly
so we need to upselling */

with repeat_customers_income AS(
    select customer_unique_id,sum(payment_value) as revenue_of_repeat_customers
    from customers c join orders o on 
    c.customer_id=o.customer_id join order_payments op ON
    o.order_id=op.order_id
    group by customer_unique_id
    having count(*)>1
),
total_revenue AS(
    select round(sum(payment_value),2) as total_revenue from order_payments
),
repeat_purchase_rate AS(
    select sum(r.revenue_of_repeat_customers) as total_revenue_of_repeat_customers
    from repeat_customers_income r
)
select p.total_revenue_of_repeat_customers,t.total_revenue,round(p.total_revenue_of_repeat_customers*100.0/t.total_revenue,2) as repeat_purchase_rate
from repeat_purchase_rate p,total_revenue t;

--Q3.Which cities have the highest average order value?

/* What is AOV 
AOV is Average order value=(sum of all orders/Total no of orders) now we have to find 
city wise (sum of all orders in that city/Total no of orders in that city) */

with aov AS(
select c.customer_city,sum(op.payment_value) as total_payment,
count(distinct o.order_id) as total_orders
from orders o join customers c on c.customer_id=o.customer_id join order_payments op 
on o.order_id=op.order_id 
where order_status="delivered"
group by c.customer_city
)
select customer_city,round(total_payment*1.0/total_orders,2) as average_order_value_per_city
from aov 
order by average_order_value_per_city desc
limit 10;

/* High AOV + low customers → niche premium market
Low AOV + high customers → mass market */