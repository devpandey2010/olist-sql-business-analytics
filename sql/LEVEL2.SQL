--Q1.How many customers have made more than one purchase (repeat customers)?
/* we will count the customers which have made ordered and group by using customer_unique_id to
see number of times their purchase  */

/* In the Olist dataset:

customer_id is unique per order
Actual repeat behavior is tracked by customer_unique_id
 */
with repeat_customers AS(
    SELECT COUNT(*) AS repeat_customers
FROM (
    SELECT c.customer_unique_id
    FROM customers c
    JOIN orders o 
        ON c.customer_id = o.customer_id
    GROUP BY c.customer_unique_id
    HAVING COUNT(*) > 1
) t
),
total_customers AS(
    select count(distinct customer_unique_id) as total_customers from customers
)
select r.repeat_customers,t.total_customers,r.repeat_customers*100.0/t.total_customers as percent_of_repeat_customers
from repeat_customers r,total_customers t;

--10 / 100 = 0  (not 0.1)
--SQL does:
--INTEGER ÷ INTEGER = INTEGER

--**Insights**
/* This will help us to know our repeat cusomers and 
we can apply some strategy for customer retention
we can provide them discounts,membership

If % is low:

The business is heavily dependent on new customer acquisition, 
which is costly and unsustainable long-term. 

If % is high:

Strong repeat behavior indicates customer trust and opens opportunities 
for upselling and cross-selling

Low items per order → apply cross-selling
Low AOV → apply upselling.*/
